using System;
namespace BeeCrowd.PrCsharp{

public class Tea{
    public void PR2006(){
        
        string val1 = Console.ReadLine();
        
        string val2 = Console.ReadLine();
        string[] fv = val2.Split(" ");

        int accepts = 0;
        foreach (var x in fv){
            if (x == val1){
                accepts++;
            }
        }

        Console.WriteLine(accepts);

        int accepts = 0;
    }
}
}

