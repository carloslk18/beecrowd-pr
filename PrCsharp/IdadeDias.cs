public class IdadeDias{
    
    public void PR1020(){

        int ano, mes, dia;

        var val = int.Parse(Console.ReadLine());

        ano = val / 365;
        mes = val % 365 / 30;

        if (val % 365 >= 30){
            dia = (val % 365) - (30*mes);
        }
        else{
            dia = val % 365;
        }

        Console.WriteLine($"{ano} ano(s)");
        Console.WriteLine($"{mes} mes(es)");
        Console.WriteLine($"{dia} dia(s)");

